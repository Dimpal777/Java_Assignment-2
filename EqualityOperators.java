/**
 * EqualityOperators
 */
public class EqualityOperators {

    public static void main(String[] args) {
        int a = 5;
        int b = 5;
        
        // Equal operator (==)
        if (a == b) {
            System.out.println("a is equal to b");
        } else {
            System.out.println("a is not equal to b");
        }
        
        // Not equal operator (!=)
        if (a != b) {
            System.out.println("a is not equal to b");
        } else {
            System.out.println("a is equal to b");
        }
    }
}